#!/usr/bin/env node
import { readdir, mkdir, writeFile, readFile, stat } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import sharp from 'sharp';
import heicConvert from 'heic-convert';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'src/assets/services');
const OUT = path.join(ROOT, 'public/images/services');
const MANIFEST = path.join(ROOT, 'src/data/serviceImages.ts');

const FOLDER_TO_SLUG = {
  'Flower Beds': 'flower-beds',
  'French Drains': 'french-drains',
  'Landscape Design & Installation': 'landscape-design',
  'Leveling & Grading': 'leveling-grading',
  'Mulch Installation': 'mulch-installation',
  'Paver Walkways & Patios': 'hardscaping',
  'Residential Concrete': 'residential-concrete',
  'Retaining Walls': 'retaining-walls',
  'Rock Beds': 'rock-beds',
  'Sod Installation': 'sod-installation',
  'Stone Borders': 'stone-borders',
  'Tree Service': 'tree-services',
};

const IMG_EXT = /\.(jpe?g|png|heic|webp)$/i;
const SIZES = { card: 800, gallery: 1600 };

async function loadImage(filePath) {
  const ext = path.extname(filePath).toLowerCase();
  if (ext === '.heic') {
    const inputBuffer = await readFile(filePath);
    const jpgBuffer = await heicConvert({ buffer: inputBuffer, format: 'JPEG', quality: 0.9 });
    return sharp(Buffer.from(jpgBuffer));
  }
  return sharp(filePath);
}

async function processOne(srcFile, outDir, baseName) {
  const out = {};
  for (const [variant, width] of Object.entries(SIZES)) {
    const outName = `${baseName}-${variant}.webp`;
    const outPath = path.join(outDir, outName);
    if (existsSync(outPath)) {
      out[variant] = outName;
      continue;
    }
    const img = await loadImage(srcFile);
    await img
      .rotate()
      .resize({ width, withoutEnlargement: true })
      .webp({ quality: 80 })
      .toFile(outPath);
    out[variant] = outName;
  }
  return out;
}

async function main() {
  const folders = await readdir(SRC, { withFileTypes: true });
  const manifest = {};

  for (const dirent of folders) {
    if (!dirent.isDirectory()) continue;
    const folder = dirent.name;
    const slug = FOLDER_TO_SLUG[folder];
    if (!slug) {
      console.log(`skip (no slug): ${folder}`);
      continue;
    }

    const inDir = path.join(SRC, folder);
    const outDir = path.join(OUT, slug);
    await mkdir(outDir, { recursive: true });

    const files = (await readdir(inDir)).filter((f) => IMG_EXT.test(f));
    if (files.length === 0) {
      console.log(`no images: ${folder}`);
      continue;
    }

    const favorite = files.find((f) => /favorite/i.test(f)) ?? files[0];
    const ordered = [favorite, ...files.filter((f) => f !== favorite)];

    const gallery = [];
    let card = null;

    for (const file of ordered) {
      const baseName = path
        .basename(file, path.extname(file))
        .replace(/[^a-z0-9]+/gi, '-')
        .replace(/^-|-$/g, '')
        .toLowerCase();
      try {
        const variants = await processOne(path.join(inDir, file), outDir, baseName);
        const gPath = `/images/services/${slug}/${variants.gallery}`;
        const cPath = `/images/services/${slug}/${variants.card}`;
        gallery.push(gPath);
        if (!card) card = cPath;
        process.stdout.write('.');
      } catch (err) {
        console.error(`\nfailed ${slug}/${file}:`, err.message);
      }
    }
    console.log(` ${slug} (${gallery.length})`);

    manifest[slug] = { card, gallery };
  }

  // Composite slugs: pull from existing slug pools (no new files written).
  const COMPOSITES = {
    'plant-installation': ['flower-beds', 'landscape-design', 'rock-beds'],
  };
  for (const [slug, sources] of Object.entries(COMPOSITES)) {
    const gallery = [];
    for (const src of sources) {
      if (manifest[src]) gallery.push(...manifest[src].gallery);
    }
    if (gallery.length === 0) continue;
    const card = gallery[0].replace('-gallery.webp', '-card.webp');
    manifest[slug] = { card, gallery };
    console.log(`composite ${slug} (${gallery.length})`);
  }

  const ts =
    `// AUTO-GENERATED by scripts/process-service-images.mjs — do not edit\n` +
    `export type ServiceImages = { card: string; gallery: string[] };\n` +
    `export const SERVICE_IMAGES: Record<string, ServiceImages> = ${JSON.stringify(manifest, null, 2)};\n`;
  await writeFile(MANIFEST, ts);
  console.log(`\nwrote ${MANIFEST}`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
